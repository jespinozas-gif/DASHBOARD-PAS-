def crear_grafico(codigo_doc, titulo, mostrar_leyenda=False):

    datos = df_global[
        df_global["COD_DOC"] == codigo_doc
    ].copy()

    # Mostrar desde enero hasta el mes seleccionado
    datos = datos[
        datos["MES1"] <= mes_seleccionado
    ]

    datos = datos.sort_values("MES1")

    fig = px.line()

    fig.add_scatter(
        x=datos["Mes"],
        y=datos["EJECUCION MENSUAL"] * 100,
        mode="lines+markers",
        name="Ejecución Mensual",
        line=dict(color="purple", width=3)
    )

    fig.add_scatter(
        x=datos["Mes"],
        y=datos["EJECUCION ANUAL"] * 100,
        mode="lines+markers",
        name="Ejecución Anual",
        line=dict(color="orange", width=3)
    )
    
    fig.update_layout(
        title=f"{titulo} - Acumulado",
        height=230,
        yaxis_title="Porcentaje (%)",
        xaxis_title="",
        
        showlegend=mostrar_leyenda,
        
        legend=dict(
            orientation="h",
            yanchor="top",
            y=-0.35,
            xanchor="center",
            x=0.5
        ),
        
        margin=dict(
            t=50,
            b=80
        )
    )

    return fig
