@st.cache_data
def cargar_datos():

    df = pd.read_excel("PAS_2026_JLES.xlsx")

    df.columns = df.columns.str.strip()

    df["DATE"] = pd.to_datetime(
        df["DATE"],
        dayfirst=True,
        errors="coerce"
    )

    # ------------------------
    # INDICADORES
    # ------------------------

    df["EJECUCION MENSUAL"] = (
        df["EJACU"] /
        df["METACU"]
    ).fillna(0)

    df["EJECUCION ANUAL"] = (
        df["EJACU"] /
        df["META ANUAL"]
    ).fillna(0)

    # Evitar infinitos

    df["EJECUCION MENSUAL"] = (
        df["EJECUCION MENSUAL"]
        .replace([float("inf"), -float("inf")], 0)
    )

    df["EJECUCION ANUAL"] = (
        df["EJECUCION ANUAL"]
        .replace([float("inf"), -float("inf")], 0)
    )

    return df

