def crear_grafico_individual(codigo_doc, titulo, mostrar_leyenda=False):

    datos = df_global[
        df_global["COD_DOC"] == codigo_doc
    ].copy()

    datos = datos[
        datos["MES1"] <= mes_seleccionado
    ]

    datos = datos.sort_values("MES1")

    fig = px.line()

    # Meta Individual
    fig.add_scatter(
        x=datos["Mes"],
        y=datos["METIN"],
        mode="lines+markers",
        name="Meta Individual",
        line=dict(color="green", width=3)
    )

    # Ejecutado Individual
    fig.add_scatter(
        x=datos["Mes"],
        y=datos["EJIN"],
        mode="lines+markers",
        name="Ejecutado Individual",
        line=dict(color="blue", width=3)
    )

    fig.update_layout(
        title=f"{titulo} - Individual",
        height=180,
        showlegend=mostrar_leyenda,
        margin=dict(
            t=40,
            b=20
        ),
        xaxis_title="",
        yaxis_title=""
    )

    return fig

